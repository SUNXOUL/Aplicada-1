using System;

public class Persona{

    public String Nombre;
    public String Apellido;
    public int Edad;
    public String Titulo;

    public Persona(String Nombre, String Apellido, int Edad, String Titulo)
    {
        this.Nombre = Nombre;
        this.Apellido = Apellido;
        this.Edad= Edad;
        this.Titulo= Titulo;
    }
    
}